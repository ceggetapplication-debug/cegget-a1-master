export const normalizeText = (text: string): string => {
    let normalized = text
        .toLowerCase()
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/['"-]/g, '')
        .replace(/\s+/g, ' ')
        .trim();

    return normalized;
};


const removeKeywordAndClean = (fullText: string, keywordToRemove: string): string => {
    const regex = new RegExp(`\\b${keywordToRemove}\\b`, 'g');
    return fullText
        .replace(regex, ' ')
        .replace(/\s+/g, ' ')
        .trim();
};

