export const estimateInputLanguage = (inputText: string): 'fr' | 'kab' | 'unknown' => {
    _ensureKeywordsAndTranslationsLoaded();

    const processedInput = normalizeText(inputText);
    const inputWords = processedInput.split(' ').filter(word => word.length > 0);
    if (inputWords.length === 0) {
        return 'unknown';
    }

    if (_loadedProductKeywords === null) {
        console.error("DEBUG LANG EST: ERREUR CRITIQUE: _loadedProductKeywords est null après _ensureKeywordsAndTranslationsLoaded. Veuillez vérifier la fonction loadTranslationsFromJson.");
        return 'unknown';
    }

    if (inputWords.length === 1 && _allProductSearchData) {
        const singleInputWord = processedInput;
        const singleInputWordVariantsKab = getLanguageSpecificSearchVariants(singleInputWord, 'kab');
        for (const productNameKey of Object.values(ProductNameKey)) {
            const translatedProductName = productNameTranslator(productNameKey, 'kab');
            const productNameVariants = getLanguageSpecificSearchVariants(translatedProductName, 'kab');
            if (Array.from(singleInputWordVariantsKab).some(v => productNameVariants.has(v))) {
                console.log(`DEBUG LANG EST: Priorité Kabyle forcée (mot unique) pour correspondance directe Nom de Product: "${translatedProductName}"`);
                return 'kab';
            }
        }
    }

    let scoreFr = 0;
    let scoreKab = 0;

    const allFrKeywords = new Set<string>();
    for (const productNameKey in _loadedProductKeywords.fr) {
        _loadedProductKeywords.fr[productNameKey as ProductNameKey].forEach(keyword => allFrKeywords.add(keyword));
    }
    const allKabKeywords = new Set<string>();
    for (const productNameKey in _loadedProductKeywords.kab) {
        _loadedProductKeywords.kab[productNameKey as ProductNameKey].forEach(keyword => allKabKeywords.add(keyword));
    }

    console.log("DEBUG LANG EST: Début du scoring détaillé pour:", processedInput);
    for (const inputWord of inputWords) {
        const inputWordVariantsFr = getLanguageSpecificSearchVariants(inputWord, 'fr');
        const inputWordVariantsKab = getLanguageSpecificSearchVariants(inputWord, 'kab');
        const allFrKeywordsArray = Array.from(allFrKeywords);
        const allKabKeywordsArray = Array.from(allKabKeywords);


        let matchedInFr = false;
        for (const inputVariant of Array.from(inputWordVariantsFr)) {
            for (const frKeyword of allFrKeywordsArray) {
                if (frKeyword === inputVariant || frKeyword.includes(inputVariant) || inputVariant.includes(frKeyword)) {
                    matchedInFr = true;
                    scoreFr += inputWord.length;
                    console.log(`DEBUG LANG EST: '${inputVariant}' (de '${inputWord}') match FR dans '${frKeyword}'. scoreFr=${scoreFr}`);
                    break;
                }
            }
            if (matchedInFr) break;
        }
        let matchedInKab = false;
        for (const inputVariant of Array.from(inputWordVariantsKab)) {
            for (const kabKeyword of allKabKeywordsArray) {
                if (kabKeyword === inputVariant || kabKeyword.includes(inputVariant) || inputVariant.includes(kabKeyword)) {
                    matchedInKab = true;
                    scoreKab += inputWord.length;
                    console.log(`DEBUG LANG EST: '${inputVariant}' (de '${inputWord}') match KAB dans '${kabKeyword}'. scoreKab=${scoreKab}`);
                    break;
                }
            }
            if (matchedInKab) break;
        }
    }
    console.log(`DEBUG LANG EST: Scores finaux: FR=${scoreFr}, KAB=${scoreKab}`);

    if (scoreKab > scoreFr) {
        return 'kab';
    } else if (scoreFr > scoreKab) {
        return 'fr';
    } else if (scoreFr > 0 && scoreKab > 0 && scoreFr === scoreKab) {
        return 'kab';
    } else if (scoreKab > 0) {
        return 'kab';
    } else if (scoreFr > 0) {
        return 'fr';
    } else {
        return 'unknown';
    }
};

export const checkExactClassificationMatch = (
    inputText: string,
    displayLanguage: 'fr' | 'kab'
): { matchType: 'productName' | 'none'; key: ProductNameKey | null } => {
    try {
        _ensureKeywordsAndTranslationsLoaded();
    } catch (e) {
        console.error("Erreur critique d'initialisation dans checkExactClassificationMatch :", e);
        return { matchType: 'none', key: null };
    }

    const normalizedInput = normalizeText(inputText);

    for (const productNameKey of Object.values(ProductNameKey)) {
        if (productNameKey === ProductNameKey.NotFound) {
            continue;
        }

        const translatedProductName = productNameTranslator(productNameKey, displayLanguage);
        const productNameVariants = getLanguageSpecificSearchVariants(translatedProductName, displayLanguage);

        if (productNameVariants.has(normalizedInput)) {
            return { matchType: 'productName', key: productNameKey };
        }
    }

    return { matchType: 'none', key: null };
};
