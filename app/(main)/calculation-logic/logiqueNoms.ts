type ScoredSuggestion = {
    productNameKey: ProductNameKey;
    translatedName: string;
    score: number;
    matchType: 'exact_term_match'
    | 'input_full_word_in_term'
    | 'term_full_word_in_input'
    | 'input_starts_term'
    | 'term_starts_input'
    | 'input_contains_term'
    | 'term_contains_input'
    | 'short_input_substring_match'
    | 'no_match';
    isTranslatedNameSingleWord: boolean;
    matchingKeyword: string;
    matchLength: number;
    optionnelle: boolean;
};

const matchTypePriority: { [key in ScoredSuggestion['matchType']]: number } = {
    'exact_term_match': 140,
    'input_full_word_in_term': 90,
    'term_full_word_in_input': 80,
    'input_starts_term': 70,
    'term_starts_input': 60,
    'input_contains_term': 50,
    'term_contains_input': 40,
    'short_input_substring_match': 3,
    'no_match': 0
};

