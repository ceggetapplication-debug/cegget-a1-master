export const getLanguageSpecificSearchVariants = (text: string, lang: 'fr' | 'kab'): Set<string> => {
    const finalVariants = new Set<string>();

    const initialNormalizedText = normalizeText(text);

    if (initialNormalizedText.trim() === '') {
        return finalVariants;
    }

    finalVariants.add(initialNormalizedText);

    const rules = LANGUAGE_SPECIFIC_VARIANT_RULES[lang];

    if (!rules || rules.length === 0) {
        return finalVariants;
    }

    console.log(`DEBUG VARIANTS (${lang}): --- Démarrage pour l'entrée: "${text}" (normalisée: "${initialNormalizedText}") ---`);

    for (const rule of rules) {
        for (const replacementOption of rule.replace) {
            const newVariant = initialNormalizedText.replace(rule.find, replacementOption);

            const normalizedNewVariant = normalizeText(newVariant);

            if (normalizedNewVariant !== initialNormalizedText && normalizedNewVariant.trim() !== '') {
                finalVariants.add(normalizedNewVariant);
                console.log(`DEBUG VARIANTS (${lang}):     - Règle "${rule.find}" => "${replacementOption}" appliquée à "${initialNormalizedText}" a product "${normalizedNewVariant}"`);
            }
        }
    }

    console.log(`DEBUG VARIANTS (${lang}): --- Variantes finales pour "${text}": [${Array.from(finalVariants).join(', ')}] ---`);
    return finalVariants;
};

