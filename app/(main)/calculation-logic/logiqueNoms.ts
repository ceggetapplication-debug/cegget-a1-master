export const productNameTranslator = (key: ProductNameKey | string, targetLanguage: 'fr' | 'kab'): string => {
    if (_allTranslationsData === null || !_allTranslationsData[targetLanguage]) {
        console.warn(`Traductions pour la langue '${targetLanguage}' non chargées.`);
        return key.toString();
    }
    const lookupKey = key.toString();
    return _allTranslationsData[targetLanguage][lookupKey] || lookupKey;
};

const _generateSearchTermsForContext = (
    productContext: ProductContextItem,
    lang: 'fr' | 'kab'
): Set<string> => {
    const terms = new Set<string>();
    const { productNameKey } = productContext;

    const addAllVariantsAndComponents = (text: string | undefined | null) => {
        if (text && text.trim() !== '') {
            getLanguageSpecificSearchVariants(text, lang).forEach(v => terms.add(v));

            const components = text.split(/[\s-]+/).filter(c => c.length > 1);
            components.forEach(component => {
                getLanguageSpecificSearchVariants(component, lang).forEach(v => terms.add(v));
            });
        }
    };
    addAllVariantsAndComponents(productNameTranslator(productNameKey, lang));


    const productNameEnumName = (Object.keys(ProductNameKey) as Array<keyof typeof ProductNameKey>).find(key => ProductNameKey[key] === productNameKey);
    if (productNameEnumName) {
        addAllVariantsAndComponents(productNameEnumName);
    }

    console.log(`DEBUG TERMS (${lang}): Termes de recherche générés pour ${productNameKey}: [${Array.from(terms).join(', ')}]`);
    return terms;
};


export const getProductSuggestions = (
    inputText: string,
    displayLanguage: 'fr' | 'kab',
    limit: number = 10
): Array<{
    productNameKey: ProductNameKey;
    translatedName: string;
}> => {
    _ensureKeywordsAndTranslationsLoaded();

    const lookups = _allProductSearchData!;
    const processedInput = normalizeText(inputText);

    console.log("--- getProductSuggestions Debugging ---");
    console.log("Input text:", inputText);
    console.log("Normalized input:", processedInput);
    console.log("DEBUG: displayLanguage = ", displayLanguage);

    if (!processedInput) {
        return [];
    }

    const allInputVariants = getLanguageSpecificSearchVariants(inputText, displayLanguage);
    console.log(`DEBUG SUGGESTIONS: Toutes les variantes générées pour l'entrée: [${Array.from(allInputVariants).join(', ')}]`);

    const isVeryShortInput = processedInput.length >= 2 && processedInput.length <= 3;
    const inputTokens = processedInput.split(' ').filter(token => token.length > 0);

    const tempProductSuggestions: ScoredSuggestion[] = [];
    const uniqueProductContextKeys = new Set<string>();
    for (const productContext of lookups.allSearchableProducts) {
        const { productNameKey } = productContext;
        if (productNameKey === ProductNameKey.NotFound) continue;


        const currentProductContextKey = `${productNameKey}`;
        if (uniqueProductContextKeys.has(currentProductContextKey)) {
            continue;
        }

        const translatedProductName = productNameTranslator(productNameKey, displayLanguage);
        const normalizedTranslatedProductName = normalizeText(translatedProductName);
        const productSearchTermsKey = `${currentProductContextKey}-${displayLanguage}`;
        const productSearchTerms = _precomputedSearchTerms!.get(productSearchTermsKey);

        if (!productSearchTerms || productSearchTerms.size === 0) {
            continue;
        }

        let bestMatchScoreForThisProduct = 0;
        let bestMatchTypeForThisProduct: ScoredSuggestion['matchType'] = 'no_match';
        let bestMatchingKeywordForThisProduct: string = '';
        let bestMatchLengthForThisProduct: number = 0;

        const allInputVariantsArray = Array.from(allInputVariants);
        const productSearchTermsArray = Array.from(productSearchTerms);

        for (const inputVariant of allInputVariantsArray) {
            if (!inputVariant) continue;

            for (const productTerm of productSearchTermsArray) {
                if (!productTerm) continue;

                let currentMatchType: ScoredSuggestion['matchType'] = 'no_match';

                if (inputVariant === productTerm) { currentMatchType = 'exact_term_match'; }
                else if (new RegExp(`\\b${inputVariant}\\b`).test(productTerm)) { currentMatchType = 'input_full_word_in_term'; }
                else if (new RegExp(`\\b${productTerm}\\b`).test(inputVariant)) { currentMatchType = 'term_full_word_in_input'; }
                else if (productTerm.startsWith(inputVariant)) { currentMatchType = 'input_starts_term'; }
                else if (inputVariant.startsWith(productTerm)) { currentMatchType = 'term_starts_input'; }

                if (currentMatchType !== 'no_match') {
                    let score = 0;
                    score = matchTypePriority[currentMatchType] * 1000;
                    score += Math.max(inputVariant.length, productTerm.length) * 10;

                    if (inputVariant === productTerm) {
                        score += 50000;
                    } else if (productTerm.startsWith(inputVariant)) {
                        score += 15000;
                    } else if (inputVariant.startsWith(productTerm)) {
                        score += 10000;
                    }

                    if (score > bestMatchScoreForThisProduct) {
                        bestMatchScoreForThisProduct = score;
                        bestMatchTypeForThisProduct = currentMatchType;
                        bestMatchingKeywordForThisProduct = productTerm;
                        bestMatchLengthForThisProduct = productTerm.length;
                    }
                }
            }
        }

        if (inputTokens.length > 1) {
            let multiTokenAccumulatedScore = 0;
            let tokensMatchedCount = 0;
            let lastTokenIsStrongProductNamePrefix = false;
            let productTypeOrCategoryMatched = false;

            const effectiveInputTokenCount = inputTokens.length;

            for (let i = 0; i < inputTokens.length; i++) {
                const token = inputTokens[i];
                let tokenHasMatchedInAnyWayForThisProduct = false;

                if (normalizedTranslatedProductName.includes(token)) {
                    multiTokenAccumulatedScore += matchTypePriority['input_contains_term'] * 20;
                    multiTokenAccumulatedScore += token.length * 5;
                    tokenHasMatchedInAnyWayForThisProduct = true;
                    if (i === inputTokens.length - 1 && normalizedTranslatedProductName.startsWith(token)) {
                        multiTokenAccumulatedScore += 5000;
                        lastTokenIsStrongProductNamePrefix = true;
                    }
                }

                if (tokenHasMatchedInAnyWayForThisProduct) {
                    tokensMatchedCount++;
                }
            }

            if (tokensMatchedCount > 0) {
                let multiTokenBaseBonus = 0;
                if (tokensMatchedCount === effectiveInputTokenCount && lastTokenIsStrongProductNamePrefix && productTypeOrCategoryMatched) {
                    multiTokenBaseBonus = 50000;
                    bestMatchTypeForThisProduct = 'exact_term_match';
                } else if (tokensMatchedCount === effectiveInputTokenCount && lastTokenIsStrongProductNamePrefix) {
                    multiTokenBaseBonus = 30000;
                    bestMatchTypeForThisProduct = 'input_full_word_in_term';
                } else if (tokensMatchedCount === effectiveInputTokenCount) {
                    multiTokenBaseBonus = 15000;
                    bestMatchTypeForThisProduct = 'input_full_word_in_term';
                } else if (tokensMatchedCount >= Math.floor(effectiveInputTokenCount / 2) && lastTokenIsStrongProductNamePrefix) {
                    multiTokenBaseBonus = 10000;
                    bestMatchTypeForThisProduct = 'term_contains_input';
                } else if (tokensMatchedCount >= Math.floor(effectiveInputTokenCount / 2)) {
                    multiTokenBaseBonus = 5000;
                    bestMatchTypeForThisProduct = 'term_contains_input';
                }

                multiTokenAccumulatedScore += multiTokenBaseBonus;

                if (multiTokenAccumulatedScore > bestMatchScoreForThisProduct) {
                    bestMatchScoreForThisProduct = multiTokenAccumulatedScore;
                }
            }
        }
        if (normalizedTranslatedProductName === processedInput) {
            bestMatchScoreForThisProduct += 50000;
        } else if (normalizedTranslatedProductName.startsWith(processedInput)) {
            bestMatchScoreForThisProduct += 10000;
        } else if (new RegExp(`\\b${processedInput}\\b`).test(normalizedTranslatedProductName)) {
            bestMatchScoreForThisProduct += 5000;
        }

        if (bestMatchScoreForThisProduct > 0) {
            tempProductSuggestions.push({
                productNameKey: productNameKey,
                translatedName: translatedProductName,
                score: bestMatchScoreForThisProduct,
                matchType: bestMatchTypeForThisProduct,
                isTranslatedNameSingleWord: translatedProductName.split(' ').length === 1,
                matchingKeyword: bestMatchingKeywordForThisProduct,
                matchLength: bestMatchLengthForThisProduct,
                optionnelle: false,
            });
            uniqueProductContextKeys.add(currentProductContextKey);
        }
    }

    if (tempProductSuggestions.length > 0) {
        const uniqueSuggestionsByTranslatedName = new Map<string, ScoredSuggestion>();
        for (const suggestion of tempProductSuggestions) {
            const existingSuggestion = uniqueSuggestionsByTranslatedName.get(suggestion.translatedName);
            if (!existingSuggestion || suggestion.score > existingSuggestion.score) {
                uniqueSuggestionsByTranslatedName.set(suggestion.translatedName, suggestion);
            }
        }
        const deDuplicatedSuggestions = Array.from(uniqueSuggestionsByTranslatedName.values());

        const sortedProductSuggestions = deDuplicatedSuggestions
            .sort((a, b) => {
                const inputIsSingleWord = processedInput.split(' ').length === 1;

                if (b.score !== a.score) {
                    return b.score - a.score;
                }

                const priorityA = matchTypePriority[a.matchType];
                const priorityB = matchTypePriority[b.matchType];
                if (priorityB !== priorityA) {
                    return priorityB - priorityA;
                }

                if (a.translatedName.length !== b.translatedName.length) {
                    if (inputIsSingleWord) {
                        return a.translatedName.length - b.translatedName.length;
                    } else {
                        return b.translatedName.length - a.translatedName.length;
                    }
                }

                return a.translatedName.localeCompare(b.translatedName, displayLanguage);
            })
            .slice(0, limit)
            .map(s => ({
                productNameKey: s.productNameKey,
                translatedName: s.translatedName,
            }));

        console.log("Suggestions triées finales (noms de products spécifiques):", sortedProductSuggestions);
        return sortedProductSuggestions;
    }
    return [];
};

const _findBestProductMatch = (normalizedInput: string, displayLanguage: 'fr' | 'kab'): ProductMatchResult | null => {
    _ensureKeywordsAndTranslationsLoaded();
    if (_loadedProductKeywords === null ||
        _allProductSearchData === null) {
        return null;
    }

    let bestMatch: ProductMatchResult | null = null;

    for (const productContext of _allProductSearchData.allSearchableProducts) {
        const { productNameKey } = productContext;
        if (productNameKey === ProductNameKey.NotFound) continue;

        const translatedProductName = productNameTranslator(productNameKey, displayLanguage);
        const productNameSearchTerms = Array.from(getLanguageSpecificSearchVariants(translatedProductName, displayLanguage));

        for (const term of productNameSearchTerms) {
            if (normalizedInput.includes(term) && term.length > (bestMatch?.matchLength || 0)) {
                bestMatch = {
                    productNameKey: productNameKey,
                    matchingKeyword: term,
                    matchLength: term.length,
                };
            }
        }
    }

    return bestMatch;
};


export const loadTranslationsFromJson = async (): Promise<void> => {
    if (_allTranslationsData !== null) {
        return;
    }
    console.time("loadTranslationsFromJson total");

    try {
        _allTranslationsData = {};

        console.time("Load fr.json");
        const frData = require('../translations/data/fr.json') as TranslationsData;
        if (!frData || Object.keys(frData).length === 0) {
            throw new Error("Les définitions linguistiques pour le français sont vides ou malformées.");
        }
        _allTranslationsData['fr'] = { ...frData };
        console.timeEnd("Load fr.json");

        console.time("Load kab.json");
        const kabData = require('../translations/data/kab.json') as TranslationsData;
        _allTranslationsData['kab'] = {};

        if (kabData && Object.keys(kabData).length > 0) {
            Object.assign(_allTranslationsData['kab'], kabData);
        }
        console.timeEnd("Load kab.json");

        console.time("_buildInternalProductSearchData");
        _buildInternalProductSearchData();
        console.timeEnd("_buildInternalProductSearchData");

        console.time("Precompute _precomputedSearchTerms");
        if (_allProductSearchData) {
            _precomputedSearchTerms = new Map();
            for (const context of _allProductSearchData.allSearchableProducts) {
                const uniqueKeyFr = `${context.productNameKey}-fr`;
                const uniqueKeyKab = `${context.productNameKey}-kab`;

                _precomputedSearchTerms.set(uniqueKeyFr, _generateSearchTermsForContext(context, 'fr'));
                _precomputedSearchTerms.set(uniqueKeyKab, _generateSearchTermsForContext(context, 'kab'));
            }
        } else {
            throw new Error("All product search data not built.");
        }
        console.timeEnd("Precompute _precomputedSearchTerms");


        if (_loadedProductKeywords === null) {
            _loadedProductKeywords = {
                fr: {} as Record<ProductNameKey, string[]>,
                kab: {} as Record<ProductNameKey, string[]>
            };

            const productNames = Object.values(ProductNameKey) as ProductNameKey[];

            console.time("Precompute _loadedProductKeywords");
            for (let i = 0; i < productNames.length; i++) {
                const productNameKey = productNames[i];

                _loadedProductKeywords.fr[productNameKey] = [];
                _loadedProductKeywords.kab[productNameKey] = [];

                const processTerms = (lang: 'fr' | 'kab') => {
                    const termsToAddForNormalization = new Set<string>();

                    const translatedProductName = productNameTranslator(productNameKey, lang);
                    if (translatedProductName && translatedProductName !== productNameKey.toString()) {
                        termsToAddForNormalization.add(translatedProductName);
                    } else if (lang === 'fr') {
                        termsToAddForNormalization.add(normalizeText(productNameKey.toString()));
                    }

                    const keywordsForLang = lang === 'fr' ? _loadedProductKeywords!.fr : _loadedProductKeywords!.kab;
                    if (!keywordsForLang[productNameKey]) {
                        keywordsForLang[productNameKey] = [];
                    }
                    const termsArray = Array.from(termsToAddForNormalization);
                    for (const term of termsArray) {
                        getLanguageSpecificSearchVariants(term, lang).forEach(v => keywordsForLang[productNameKey].push(v));
                    }
                };

                processTerms('fr');
                processTerms('kab');

                _loadedProductKeywords!.fr[productNameKey] = Array.from(new Set(_loadedProductKeywords!.fr[productNameKey]));
                _loadedProductKeywords!.kab[productNameKey] = Array.from(new Set(_loadedProductKeywords!.kab[productNameKey]));
            }
            console.timeEnd("Precompute _loadedProductKeywords");
        }
    } catch (error) {
        _allTranslationsData = null;
        _allProductSearchData = null;
        _loadedProductKeywords = null;
        console.error("Error during loadTranslationsFromJson:", error);
        throw error;
    }
    console.timeEnd("loadTranslationsFromJson total");
};

const _buildInternalProductSearchData = (): void => {
    if (_allProductSearchData) {
        return;
    }
    console.time("_buildInternalProductSearchData execution");

    const productNameToContexts: Record<ProductNameKey, Array<ProductContextItem>> = {} as Record<ProductNameKey, Array<ProductContextItem>>;
    const allSearchableProducts: Array<ProductContextItem> = [];

    for (const pName of Object.values(ProductNameKey)) {
        if (pName === ProductNameKey.NotFound) {
            continue;
        }

        const contextItem: ProductContextItem = { productNameKey: pName };

        if (!productNameToContexts[pName]) {
            productNameToContexts[pName] = [];
        }
        productNameToContexts[pName].push(contextItem);

        allSearchableProducts.push(contextItem);
    }

    _allProductSearchData = {
        productNameToContexts,
        allSearchableProducts,
    };

    console.timeEnd("_buildInternalProductSearchData execution");
};

export const recognizeProductClassification = async (inputText: string, displayLanguage: 'fr' | 'kab'): Promise<{
    productNameKey: ProductNameKey;
    productNameTranslated: string
}> => {
    _ensureKeywordsAndTranslationsLoaded();
    if (_allTranslationsData === null ||
        _allProductSearchData === null ||
        _loadedProductKeywords === null) {
        throw new Error("Translations data or product search data not initialized. Please call loadTranslationsFromJson first.");
    }

    if (!inputText || inputText.trim() === '') {
        const noProductNameKey = ProductNameKey.NotFound;
        return {
            productNameKey: noProductNameKey,
            productNameTranslated: productNameTranslator(noProductNameKey, displayLanguage)
        };
    }

    const originalInputNormalized = normalizeText(inputText);
    const bestMatch = _findBestProductMatch(originalInputNormalized, displayLanguage);

    let finalProductNameKey: ProductNameKey = ProductNameKey.NotFound;
    let rawProductNameExtracted: string = originalInputNormalized;

    if (bestMatch) {
        finalProductNameKey = bestMatch.productNameKey;
        rawProductNameExtracted = removeKeywordAndClean(originalInputNormalized, bestMatch.matchingKeyword);
    } else {
        rawProductNameExtracted = originalInputNormalized;
    }

    if (finalProductNameKey === ProductNameKey.NotFound && rawProductNameExtracted !== '') {
        const potentialKey = Object.values(ProductNameKey).find(key =>
            normalizeText(key.toString()) === rawProductNameExtracted);
        finalProductNameKey = potentialKey || ProductNameKey.NotFound;
    } else if (
        finalProductNameKey === ProductNameKey.NotFound && rawProductNameExtracted === '') {
        finalProductNameKey = ProductNameKey.NotFound;
    }

    return {
        productNameKey: finalProductNameKey,
        productNameTranslated: productNameTranslator(finalProductNameKey, displayLanguage)
    }
};

